package Model;

public class Model {
    private Circuits circuits;
    private ConstructorResults constructorResults;
    private ConstructorStandings constructorStandings;
    private Constructors constructors;
    private Drivers drivers;
    private LapTimes lapTimes;
    private PitStops pitStops;
    private Qualifying qualifying;
    private Races races;
    private Results results;
    private Seasons seasons;
    private Status status;

    public Circuits getCircuits() {
        return circuits;
    }

    public void setCircuits(Circuits circuits) {
        this.circuits = circuits;
    }

    public ConstructorResults getConstructorResults() {
        return constructorResults;
    }

    public void setConstructorResults(ConstructorResults constructorResults) {
        this.constructorResults = constructorResults;
    }

    public ConstructorStandings getConstructorStandings() {
        return constructorStandings;
    }

    public void setConstructorStandings(ConstructorStandings constructorStandings) {
        this.constructorStandings = constructorStandings;
    }

    public Constructors getConstructors() {
        return constructors;
    }

    public void setConstructors(Constructors constructors) {
        this.constructors = constructors;
    }

    public Drivers getDrivers() {
        return drivers;
    }

    public void setDrivers(Drivers drivers) {
        this.drivers = drivers;
    }

    public LapTimes getLapTimes() {
        return lapTimes;
    }

    public void setLapTimes(LapTimes lapTimes) {
        this.lapTimes = lapTimes;
    }

    public PitStops getPitStops() {
        return pitStops;
    }

    public void setPitStops(PitStops pitStops) {
        this.pitStops = pitStops;
    }

    public Qualifying getQualifying() {
        return qualifying;
    }

    public void setQualifying(Qualifying qualifying) {
        this.qualifying = qualifying;
    }

    public Races getRaces() {
        return races;
    }

    public void setRaces(Races races) {
        this.races = races;
    }

    public Results getResults() {
        return results;
    }

    public void setResults(Results results) {
        this.results = results;
    }

    public Seasons getSeasons() {
        return seasons;
    }

    public void setSeasons(Seasons seasons) {
        this.seasons = seasons;
    }

    public Status getStatus() {
        return status;
    }

    public void setStatus(Status status) {
        this.status = status;
    }
}
